        text = self.ocr.extract_text(file_path)

        medicines = self.parser.extract_medicines(text)

        cursor.execute(

        """

        INSERT INTO prescriptions(

        patient_id,

        ocr_text

        )

        VALUES(?,?)

        """,

        (

        patient_id,

        text

        )

        )

        connection.commit()

        return {

            "ocr_text": text,

            "medicines": medicines

        }

