from datetime import datetime

class EmergencyDetectionAgent:

    def __init__(self):
        pass

    # --------------------------------------------------------
    # Detect Emergency
    # --------------------------------------------------------

    def detect_emergency(self, wellness):

        severity = wellness.get("severity", "").lower()

        emergency_keywords = [
            "high",
            "severe",
            "critical"
        ]

        return severity in emergency_keywords

    # --------------------------------------------------------
    # Save Emergency Event
    # --------------------------------------------------------

    def save_event(
        self,
        patient_id,
        location
    ):

        cursor.execute(
            """
            INSERT INTO emergencies(
                patient_id,
                location
            )
            VALUES(?,?)
            """,
            (
                patient_id,
                location
            )
        )

        connection.commit()

    # --------------------------------------------------------
    # Generate Alert
    # --------------------------------------------------------

    def generate_alert(
        self,
        patient_name,
        location
    ):

        return f"""
🚨 EMERGENCY ALERT 🚨

Patient : {patient_name}

Location : {location}

Immediate medical attention may be required.

Please contact the caregiver or emergency services immediately.
"""

