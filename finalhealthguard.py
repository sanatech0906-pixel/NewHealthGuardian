import re

class PatientRegistrationAgent:

    VALID_BLOOD_GROUPS = {
        "A+","A-","B+","B-",
        "AB+","AB-","O+","O-"
    }

    def __init__(self):
        pass

    # --------------------------------------------------------

    def validate_name(self, name):

        if len(name.strip()) < 2:
            raise ValueError("Invalid Name")

        return name.title()

    # --------------------------------------------------------

    def validate_age(self, age):

        age = int(age)

        if age <= 0 or age > 120:
            raise ValueError("Invalid Age")

        return age

    # --------------------------------------------------------

    def validate_gender(self, gender):

        gender = gender.title()

        allowed = ["Male","Female","Other"]

        if gender not in allowed:
            raise ValueError("Invalid Gender")

        return gender

    # --------------------------------------------------------

    def validate_blood_group(self, bg):

        bg = bg.upper()

        if bg not in self.VALID_BLOOD_GROUPS:
            raise ValueError("Invalid Blood Group")

        return bg

    # --------------------------------------------------------

    def validate_phone(self, phone):

        phone = str(phone)

        if not re.fullmatch(r"\d{10}", phone):
            raise ValueError("Invalid Phone Number")

        return phone

    # --------------------------------------------------------

    def register_patient(self, profile):

        cursor.execute("""

        INSERT INTO patients(

        name,
        age,
        gender,
        blood_group,
        height,
        weight,
        allergies,
        category,
        doctor_name,
        doctor_contact,
        emergency_contact

        )

        VALUES(?,?,?,?,?,?,?,?,?,?,?)

        """,

        (

        self.validate_name(profile["name"]),

        self.validate_age(profile["age"]),

        self.validate_gender(profile["gender"]),

        self.validate_blood_group(profile["blood_group"]),

        profile["height"],

        profile["weight"],

        profile["allergies"],

        profile["category"],

        profile["doctor_name"],

        self.validate_phone(profile["doctor_contact"]),

        self.validate_phone(profile["emergency_contact"])

        )

        )

        connection.commit()

        patient_id = cursor.lastrowid

        return patient_id


