from datetime import datetime, timedelta

class MedicineReminderAgent:

    def __init__(self):
        pass

    # --------------------------------------------------------
    # Generate Reminder Times
    # --------------------------------------------------------

    def generate_schedule(self, frequency):

        frequency = frequency.lower()

        if "once" in frequency:
            return ["08:00"]

        elif "twice" in frequency:
            return ["08:00", "20:00"]

        elif "three" in frequency:
            return ["08:00", "14:00", "20:00"]

        elif "four" in frequency:
            return ["06:00", "12:00", "18:00", "22:00"]

        else:
            return ["08:00"]

    # --------------------------------------------------------
    # Calculate Refill Date
    # --------------------------------------------------------

    def calculate_refill_date(self, tablets, frequency):

        frequency = frequency.lower()

        if "once" in frequency:
            doses = 1

        elif "twice" in frequency:
            doses = 2

        elif "three" in frequency:
            doses = 3

        elif "four" in frequency:
            doses = 4

        else:
            doses = 1

        days = tablets // doses

        refill_date = datetime.now() + timedelta(days=days)

        return refill_date.strftime("%Y-%m-%d")

    # --------------------------------------------------------
    # Save Medicine
    # --------------------------------------------------------

    def save_medicine(self, patient_id, medicine):

        schedule = self.generate_schedule(
            medicine["frequency"]
        )

        refill_date = self.calculate_refill_date(
            medicine.get("tablets", 30),
            medicine["frequency"]
        )

        cursor.execute(
            """
            INSERT INTO medicines(
                patient_id,
                medicine_name,
                dosage,
                frequency,
                duration,
                tablets,
                refill_date
            )
            VALUES(?,?,?,?,?,?,?)
            """,
            (
                patient_id,
                medicine["medicine_name"],
                medicine["dosage"],
                medicine["frequency"],
                medicine["duration"],
                medicine.get("tablets", 30),
                refill_date
            )
        )

        connection.commit()

        return {
            "medicine_name": medicine["medicine_name"],
            "schedule": schedule,
            "refill_date": refill_date
        }

