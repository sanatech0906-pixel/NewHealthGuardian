class PatientCategoryAgent:

    CATEGORIES = [
        "Elderly Citizen",
        "Chronic Disease Patient",
        "Accident Recovery",
        "Pre-Surgery",
        "Post-Surgery",
        "Long-term Medication",
        "General Health"
    ]

    def __init__(self):
        pass

    # --------------------------------------------------------

    def recommend_category(self, profile):

        age = profile.get("age", 0)

        allergies = profile.get("allergies", "").lower()

        if age >= 60:
            return "Elderly Citizen"

        elif "diabetes" in allergies or "hypertension" in allergies:
            return "Chronic Disease Patient"

        else:
            return "General Health"

    # --------------------------------------------------------

    def validate_category(self, category):

        if category not in self.CATEGORIES:
            raise ValueError("Invalid Patient Category")

        return category

    # --------------------------------------------------------

    def update_database(self, patient_id, category):

        cursor.execute(
            """
            UPDATE patients
            SET category=?
            WHERE id=?
            """,
            (category, patient_id)
        )

        connection.commit()

